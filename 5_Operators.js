// Operators in js..
// airthmetic operators..
let a = 20
let b = 3
console.log("a + b =", a+b)
console.log("a - b =", a-b)
console.log("a * b =", a*b)
console.log("a / b =", a/b)
console.log("a ** b =", a**b)
console.log("a % b =", a%b)
console.log("++a  =", ++a) // print first then increment
console.log("a++  =", a++)// print first then decrement 
console.log("--a  =", --a)// add the number then print it
console.log("a--  =", a--)// subtract then print it
console.log ("a", a)

// assignment operators..
let a1 = 5
a1 += 5
console.log(a1)
a1 -= 5
console.log(a1)
// Comparison operators
let com1 = 6
let com2 = "6"
console.log("com1 == com2 is", com1 == com2)
console.log("com1 != com2 is", com1 != com2)
console.log("com1 === com2 is", com1 === com2) //it checks the type as well as the value. 
console.log("com1 !== com2 is", com1 !== com2)
// Logical operators
let x = 5
let y = 6
console.log(x<y && x==5) //both the cases should be true 
console.log(x>y || x==5) //any one of the cases should be true
console.log(!false) // it prints the opposite of false
console.log(!true) // it prints the opposite of true

